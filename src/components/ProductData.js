const getProductData = (category = "ALL", onlyBest4Items = false) => {
  if (category === "ALL") {
    return ProductData;
  } else {
    if (onlyBest4Items) {
      return getBest4Items(category);
    } else {
      return ProductData.filter((item) => item.category === category);
    }
  }
};

const getBest4Items = (category) => {
  return ProductData.filter((item) => item.category === category).slice(0, 4);
};

const getProductDetail = (itemId) => {
  return ProductData.find((item) => item.id === parseInt(itemId));
};

const eCommerceApi = {
  getProductData,
  getProductDetail,
};

export default eCommerceApi;
